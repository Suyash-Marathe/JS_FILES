let grade;

grade = prompt("Enter The Grade : ");

if (grade >= 90)
{
	document.write("Grade A+");
	console.log("Grade A+");
}
else if (grade >= 70)
{
	document.write("Grade A");
	console.log("Grade A");
}
else if (grade >= 30)
{
	document.write("Grade B");
	console.log("Grade B");
}
else if (grade < 30)
{
	document.write("Grade C");
	console.log("Grade C");
}